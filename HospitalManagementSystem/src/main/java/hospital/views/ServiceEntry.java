    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (serviceID == null) {
        JOptionPane.showMessageDialog(this, "No service selected to delete.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this service?", "Confirm", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) return;

    try {
        Connection con = hospital.db.ConnectDB.ConnectDB();
        String sql = "DELETE FROM services WHERE service_id = ?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setInt(1, serviceID);
        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Service deleted.");
        clearFields();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error deleting service: " + e.getMessage());
    }
            // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
          if (serviceID == null) {
        JOptionPane.showMessageDialog(this, "No service selected for update.");
        return;
    }

    try {
        Connection con = hospital.db.ConnectDB.ConnectDB();
        String sql = "UPDATE services SET service_name = ?, service_charge = ? WHERE service_id = ?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, txtServiceName.getText());
        pst.setBigDecimal(2, new java.math.BigDecimal(txtServiceCharge.getText()));
        pst.setInt(3, serviceID);

        int rows = pst.executeUpdate();
        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Service updated successfully.");
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Update failed.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error updating service: " + e.getMessage());
    }
            // TODO add your handling code here:
    }//GEN-LAST:event_btnUpdateActionPerformed

