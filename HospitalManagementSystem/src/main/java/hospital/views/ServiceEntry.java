    private void loadTableData() {
        try {
            Connection conn = hospital.db.ConnectDB.ConnectDB();
            String sql = "SELECT service_id, service_name, service_charge FROM services";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            DefaultTableModel model = new DefaultTableModel(
                new String[]{"ID", "Service Name", "Service Charge"}, 0
            );
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("service_id"),
                    rs.getString("service_name"),
                    rs.getBigDecimal("service_charge")
                });
            }
            tblServices.setModel(model);

            rs.close();
            pst.close();
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
        }
    }   

